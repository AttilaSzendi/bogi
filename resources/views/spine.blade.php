            Edzés, ami segít megelőzni vagy enyhíteni a derékfájdalmat, korrigálja a hanyag tartást, és emellett még hatékony zsírégető is?

            A gerincünk egészsége nagyon fontos, olvasd végig ezt az oldalt és megtudod hogyan tudok segíteni, hogy ezzel ne legyen gondod!

            Örülök, hogy ide kattintottál, mert így tudom, hogy fontos neked az egészség, a helyes tartás.

            Sokan dolgozunk egész nap ülve, és nem is gondolunk bele, hogy ilyenkor fokozatosan rosszul terheljük a gerincünket, derék-, nyakfájás alakulhat ki, előrehelyeződhet a fejünk, tartásunk hanyaggá válhat. Reggelente egyre többször érezzük merevnek a hátunkat, és sajnos nagyon megnöveljük a porckorongsérv kialakulásának veszélyét.

            Ezeket a káros hatásokat ellensúlyoznunk kell a napi rutinunk során is, illetve megfelelő mozgással. Ebben segít ez az óra.

            A csoportos órán precízen, óvatosan dolgozunk, együtt csináljuk a gyakorlatokat. A jó hangulaton és egy kellemesen fáradt érzésen túl kapsz egy “tettem ma is magamért érzést.”

            Mi történik, ha nem teszel semmit?
            Ma a halogatás az egyik olyan tényező, ami gátol a céljaink elérésében. Ha elegem van a munkámból, mert folyamatosan dolgoznom kell és nem jut elég időm magamra, akkor nem majd “jövőhéten”, vagy a “hétvégén” kell elmenni edzeni, hanem minél előbb. Mert fel kell ismerni, hogy jövőhét meg a hétvége sajnos sok esetben a kifogásunk csak.
            Persze van olyan, hogy tényleg kivitelezhetetlen elmenni edzeni hét közben, de valljuk be, az esetek túlnyomó részében csak megtudnánk oldani ha elég fontos lenne.

            Gyere el és próbáld ki a Gerinctorna és nyújtás órát és kezd el az átalakulásod a szebb tartás, fájdalom mentes mozgások és nagyobb hajlékonyág felé!

            Miért érdemes regisztrálnod a Gerinctorna és nyújtás órámra?

            Amit kidogoztam, azért egyedülálló a többi között, mert:
            • Budapes belvárosában tartom, könnyen megközelíthető helyen, a 13. kerületben.
            • Ez egy fantasztikus hangulattal és pozitív energiákkal teli csoportos edzés, ami ugyan kemény és intenzív, de csak az ilyen jellegű edzésekkel lehet rövid idő alatt látványos eredményt elérni.
            • Ha szép tartásra vágysz.
            • Nem szeretnél merev háttal ébredni.
            • Szeretnéd enyhíteni, elmulasztani a derék- és nyakfájásod.
            • Mert nálam nem csak egy gyakorlatsort kapsz, hanem figyelmet is, hogy helyesen tudod-e csinálni a gyakorlatokat, megelőzve ezzel az esetleges sérüléseket, sportártalmakat.

            Tudj meg többet rólam.
            • Szendi Boglárka vagyok, fitness instruktor és preventív gerinc tréner. Debrecenben születtem, de már 12 éve Budapesten élek.
            • A mozgás világ életemben szerves részét képezte a mindennapjaimnak, mindig táncoltam, sportoltam valamit. Mindezt egy darabig csak hobbiként, a munkám mellett tettem, de az utóbbi években gondolkoztam, és arra jutottam, hogy több teret szeretnék adni neki. Ezért döntöttem úgy, hogy elvégzem a fitness instruktor képzést – és a jövőben még más sportképzéseket is, hogy megfelelő tudás birtokában kezdhessenek neki ennek az útnak.

            Mit kapsz a Gerinctorna és nyújtásórán?

            Az óra 5-10 perces bemelegítéssel kezdődik, hogy felkészítsük a testünket a gyakorlatokra. Ezt követően 20-25 percen keresztül megmozgatjuk, erősítjük
            a legfontosabb gerincet befoláysoló izmainkat izmainkat. Az óra második felében nyújtó gyakorlatokat végzünk, különös hangsúlyt fektetve a rövidülésre hajlamos, fájdalamak kialakulásában sokszor fontos szerepet játszó izmok megnyújtására. Az óra egyszerű, de hatékony gyakorlatokból áll, így könnyű követni, edzettségi szinttől függetlenül.
